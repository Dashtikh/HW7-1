package com.company;

public class CarEnti {

    private int model;
    private String name;
    private int price;


    public int getmodel() {
        return model;
    }

    public CarEnti setmodel(int model) {
        this.model = model;
        return this;
    }

    public String getName() {
        return name;
    }

    public CarEnti setName(String name) {
        this.name = name;
        return this;
    }

    public int getPrice() {
        return price;
    }

    public CarEnti setprice(int price) {
        this.price = price;
        return this;
    }


}
